package ex_01_java_basics;

public class Lab006_Math_println {

    public static void main(String[] args) {

        System.out.println(2 + 2);
        System.out.println(3 / 2); // Integer division, println will print only integer part
        System.out.println(3.0 / 2); // Decimal division, println will print decimal
        System.out.println(10 * 2 + 3 - 4 - 5);
    }

}
