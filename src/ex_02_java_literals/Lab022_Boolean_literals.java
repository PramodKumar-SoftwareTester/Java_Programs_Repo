package ex_02_java_literals;

public class Lab022_Boolean_literals {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean is_Java_fun = true;
        boolean is_java_not_good = false;

        System.out.println(a);  // Output: true
        System.out.println(b);  // Output: false
        System.out.println("Is Java fun? " + is_Java_fun);  // Output: Is Java fun? true
        System.out.println("Is java not good? " + is_java_not_good);  // Output: Is amit married? false
    }
}
