package ex_03_java_operators;

public class Lab028_Modulas_Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        int c = a % b; // Modulus operator gives the remainder of the division
        System.out.println("The remainder of " + a + " divided by " + b + " is: " + c);
    }
}
